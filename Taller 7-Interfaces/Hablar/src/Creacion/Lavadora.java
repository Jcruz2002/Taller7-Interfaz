package Creacion;

/**
 *
 * @author jdavi
 */
public class Lavadora extends Aparato {
    
    private int alto;
    private int ancho;

    public Lavadora(int alto, int ancho, int Consumo, int precio) {
        super(Consumo, precio);
        this.alto = alto;
        this.ancho = ancho;
    }

    /**
     * @return the alto
     */
    public int getAlto() {
        return alto;
    }

    /**
     * @param alto the alto to set
     */
    public void setAlto(int alto) {
        this.alto = alto;
    }

    /**
     * @return the ancho
     */
    public int getAncho() {
        return ancho;
    }

    /**
     * @param ancho the ancho to set
     */
    public void setAncho(int ancho) {
        this.ancho = ancho;
    }
    
    
    
}
