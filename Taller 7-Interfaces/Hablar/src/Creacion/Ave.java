package Creacion;

/**
 *
 * @author jdavi
 */
public abstract class Ave {
    
    private String Sexo;
    private int Edad;

    public Ave(String Sexo, int Edad) {
        this.Sexo = Sexo;
        this.Edad = Edad;
    }

    /**
     * @return the Sexo
     */
    public String getSexo() {
        return Sexo;
    }

    /**
     * @param Sexo the Sexo to set
     */
    public void setSexo(String Sexo) {
        this.Sexo = Sexo;
    }

    /**
     * @return the Edad
     */
    public int getEdad() {
        return Edad;
    }

    /**
     * @param Edad the Edad to set
     */
    public void setEdad(int Edad) {
        this.Edad = Edad;
    }
    
    
            
    
}
